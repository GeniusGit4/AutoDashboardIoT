/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_CAN1_Init();
  /* USER CODE BEGIN 2 */

  // Start the CAN peripheral
  if (HAL_CAN_Start(&hcan1) != HAL_OK)
  {
	  /* Start Error */
	  Error_Handler();
  }

  /* Configure Transmission process */
    TxHeader.StdId = 0x123; // This is the ID the receiver will listen for
    TxHeader.RTR = CAN_RTR_DATA;
    TxHeader.IDE = CAN_ID_STD;
    TxHeader.DLC = 2; // Data Length Code: 2 bytes
    TxHeader.TransmitGlobalTime = DISABLE;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
      // Check if the user button (PA0) is pressed
      if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET)
      {
          HAL_Delay(50); // Debouncing Delay
          if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET) // Confirm button press
          {
              // Cycle ubKeyNumber from 0 to 4 for different LED states on receiver
              if (ubKeyNumber >= 0x4) // Reset counter after 4
              {
                  ubKeyNumber = 0x00;
              }
              else
              {
                  ubKeyNumber++; // Increment counter
              }

              // Prepare data to transmit: first byte is ubKeyNumber, second is 0xAD
              TxData[0] = ubKeyNumber;
              TxData[1] = 0xAD;

              /* Start the Transmission process */
              if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox) != HAL_OK)
              {
                  /* Transmission request Error */
                  Error_Handler(); // Red LED will indicate error
              }
              else
              {
                  // Successfully queued for transmission: Indicate with green LED
                  HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, SET); // Turn on Green LED
              }

              // Wait for transmission to complete (optional, depends on application)
              // For a button press, you might not need to block here.
              // For simplicity, let's just turn off the LED after a short delay
              HAL_Delay(100); // Delay for LED to be visible
              HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, RESET); // Turn off Green LED

              // Wait until button is released to prevent multiple transmissions from one press
              while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET);
              HAL_Delay(50); // Debounce release
          }
      }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

